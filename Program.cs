int[] GetArray(int size =8){
    int[] arr = new int[size];
    for (int i = 0; i < arr.Length; i++){
        arr[i] = new Random().Next(10);
    }
    return arr;
}

foreach (var item in GetArray()){
    System.Console.WriteLine(item);
}