//Задача 27: Напишите программу, которая принимает на вход число и выдаёт сумму цифр в числе.

int GetSum(){
    int a = Convert.ToInt32(Console.ReadLine());
    int  sum = 0;
    while (a > 0){
        sum += a % 10;
        a /= 10;
    }
    return sum;
}
System.Console.WriteLine(GetSum());


