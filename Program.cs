//Задача 21
//Напишите программу, которая принимает на вход координаты двух точек 
//и находит расстояние между ними в 3D пространстве.

double GetDistance(int x1, int y1, int h1, int x2, int y2, int h2){
    return Math.Sqrt(Math.Pow(x1 - x2, 2) + Math.Pow(y1 - y2, 2) + Math.Pow(h1 - h2, 2));
}
System.Console.WriteLine(GetDistance(3,6,8,2,1,-7))



