//Задача 23
//Напишите программу, которая принимает на вход число (N) 
//и выдаёт таблицу кубов чисел от 1 до N.

int[] Get3(int num){
    int[] arr = new int[num];

    for (int i = 1; i <= num; i++){
        arr[i-1] = i * i * i;
    }
    return arr;
}
foreach (int num in Get3(5)){
    System.Console.WriteLine(num);
}