//           возводит число A в натуральную степень B.

double GetPow(){
    Console.Write("Введите число: ");
    double x = Convert.ToDouble(Console.ReadLine());
    double p = 1;
    Console.Write("Введите степерь: ");
    int y = Convert.ToInt32(Console.ReadLine());
    for (int i = 0; i < y; i++){
        p *= x;
    }
    return p;
}
